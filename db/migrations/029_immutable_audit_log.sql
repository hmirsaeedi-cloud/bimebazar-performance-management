drop policy if exists "compliance can read audit events" on public.audit_events;
create policy "compliance can read audit events"
on public.audit_events for select
to authenticated
using (
  (select app_private.current_user_has_role('HR_ADMIN'))
  or (select app_private.current_user_has_role('HRBP'))
  or actor_user_id = (select auth.uid())
  or target_user_id = (select auth.uid())
);

drop policy if exists "compliance can read audit export requests" on public.audit_export_requests;
create policy "compliance can read audit export requests"
on public.audit_export_requests for select
to authenticated
using (
  requested_by = (select auth.uid())
  or (select app_private.current_user_has_role('HR_ADMIN'))
);

drop policy if exists "hr admin can write audit export requests" on public.audit_export_requests;
create policy "hr admin can write audit export requests"
on public.audit_export_requests for all
to authenticated
using ((select app_private.current_user_has_role('HR_ADMIN')))
with check ((select app_private.current_user_has_role('HR_ADMIN')));

